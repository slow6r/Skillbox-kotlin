import exceptions.*

fun main() {
    val wheel = Wheel()
    
    // Тест 1: Попытка накачать колесо отрицательным давлением
    try {
        wheel.pump(-5.0)
        println("При накачке -5 атмосфер процедура удалась.")
        wheel.checkPressure()
        println("Эксплуатация возможна.")
    } catch (e: IncorrectPressure) {
        println("При накачке -5 атмосфер процедура не удалась.")
    } catch (e: TooHighPressure) {
        println("При накачке -5 атмосфер процедура удалась. Эксплуатация невозможна — давление превышает нормальное.")
    } catch (e: TooLowPressure) {
        println("При накачке -5 атмосфер процедура удалась. Эксплуатация невозможна — давление ниже нормального.")
    }
    
    println()
    
    // Тест 2: Попытка накачать колесо высоким давлением
    try {
        wheel.pump(6.0)
        println("При накачке 6 атмосфер процедура удалась.")
        wheel.checkPressure()
        println("Эксплуатация возможна.")
    } catch (e: IncorrectPressure) {
        println("При накачке 6 атмосфер процедура не удалась.")
    } catch (e: TooHighPressure) {
        println("При накачке 6 атмосфер процедура удалась. Эксплуатация невозможна — давление превышает нормальное.")
    } catch (e: TooLowPressure) {
        println("При накачке 6 атмосфер процедура удалась. Эксплуатация невозможна — давление ниже нормального.")
    }
    
    println()
    
    // Тест 3: Попытка накачать колесо нормальным давлением
    try {
        wheel.pump(2.0)
        println("При накачке 2 атмосферы процедура удалась.")
        wheel.checkPressure()
        println("Эксплуатация возможна.")
    } catch (e: IncorrectPressure) {
        println("При накачке 2 атмосферы процедура не удалась.")
    } catch (e: TooHighPressure) {
        println("При накачке 2 атмосферы процедура удалась. Эксплуатация невозможна — давление превышает нормальное.")
    } catch (e: TooLowPressure) {
        println("При накачке 2 атмосферы процедура удалась. Эксплуатация невозможна — давление ниже нормального.")
    }
} 